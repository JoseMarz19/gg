        <p>TRANSFERENCIAS</p>



        <p>PABEAXXA S. DE R. L. DE C.V. Hace del conocimiento del Titular que los datos personales pueden ser remitidos dentro y fuera del país, con el objeto de dar cumplimiento a las obligaciones jurídicas y/o comerciales, para lo cual hemos celebrado o celebraremos diversos acuerdos comerciales tanto en territorio nacional como en el extranjero. Los receptores de los datos personales están obligados a mantener las medidas previstas por la Legislación aplicable, el contrato correspondiente, así́ como por el presente Aviso de Privacidad, los datos personales suministrados por el responsable.</p>



        <p>Aunado a lo anterior, se hace de su conocimiento que en podremos trasferir datos personales a petición de las autoridades competentes con la finalidad de atender requerimientos que estén debidamente fundados, motivados y con un mandato legal.</p>



        <p>Ante este escenario hacemos de su conocimiento que dentro de los terceros a los que se transferirán dichos datos se incluyen dependencias de gobierno, federales, estatales y municipales, así como a cualquier empresa del mismo grupo o aquellos que PABEAXXA S. DE R. L. DE C.V. considere necesario o conveniente comunicar por una relación contractual de atención a titulares de los datos personales; esta comunicación se da manteniendo su estricta confidencialidad y responsabilidad entre las partes involucradas. Si usted no manifiesta su oposición para que sus datos sean transferidos, se entenderá que ha otorgado su consentimiento para ello.</p>



        <p>MEDIOS PARA EJERCER DERECHOS ARCO</p>



        <p>¿Cómo puede acceder, rectificar o cancelar sus datos personales, u oponerse a su uso?</p>



        <p>Usted tiene derecho a conocer qué datos personales tenemos de usted, para qué los utilizamos y las condiciones del uso que les damos (Acceso), así mismo, es su derecho solicitar la corrección de su información personal en caso de que esté desactualizada, sea inexacta o incompleta (Rectificación); que la eliminemos de nuestros registros o bases de datos cuando considere que la misma no está siendo utilizada conforme a los principios, deberes y obligaciones previstas en la normativa (Cancelación); así como oponerse al uso de sus datos personales para fines específicos (Oposición). Estos derechos se conocen como Derechos ARCO.</p>



        <p>Para el ejercicio de cualquiera de los derechos ARCO, usted deberá presentar la solicitud respectiva en el domicilio del responsable descrito en este Aviso de Privacidad por medio de un escrito libre o formato de derecho ARCO dirigida a nuestro Departamento de Protección de Datos Personales de lunes a viernes de 10:00 a 18:00 hrs solicitándole sus derechos a ejercer.</p>



        <p>Adicionalmente le solicitamos que presente la siguiente información y adjunte los documentos que se enlistan a continuación en original y copia:</p>



        <p>Nombre del titular</p>



        <p>Domicilio de titular o dirección de correo electrónico para comunicar respuesta a solicitud</p>



        <p>Documentos que acrediten identidad o autorización para representarlo en la solicitud.</p>



        <p>Descripción de datos personales sobre los que se pretende ejercer algún derecho ARCO</p>



        <p>Cualquier otro elemento que permita la localización de los datos personales y atención a la solicitud</p>



