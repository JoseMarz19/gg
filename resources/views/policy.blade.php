        <p class="has-huge-font-size">FUNDAMENTO LEGAL.</p>



        <p>El presente Aviso de Privacidad (aviso) se emite en cumplimiento por lo dispuesto por el artículo 15 y 17 de la Ley Federal de Protección de Datos Personales en Posesión de los Particulares (LFPDPPP), de los Lineamientos del Aviso de Privacidad en sus artículos 6, 9,11 y 17 y del artículo 23, 25 y 31 de su Reglamento (Principio de Información).</p>



        <p class="has-medium-font-size"><strong>CONCEPTOS</strong></p>



        <p>I.1. CONSENTIMIENTO: Manifestación de la voluntad del titular de los datos mediante la cual se efectúa el tratamiento de los mismos.</p>



        <p>I.2. DATOS PERSONALES: Cualquier información concerniente a una persona física identificada o identificable.</p>



        <p>I.3. DATOS PERSONALES SENSIBLES: Aquellos datos personales íntimos del Titular de los Datos, que por ejemplo puedan revelar aspectos raciales, étnicos, estado de salud presente o futuro, información genética, creencias religiosas, filosóficas y morales, afiliación sindical, opiniones políticas, preferencia sexual.</p>



        <p>I.4. RESPONSABLE: Persona física o moral de carácter privado que decide sobre el tratamiento de datos personales en este caso PABEAXXA S. DE R. L. DE C.V.</p>



        <p>I.5. TERCERO: La persona física o moral, nacional o extranjera, distinta del titular o del responsable de los datos.</p>



        <p>I.6. TITULAR: La persona física a quien corresponden los datos personales</p>



        <p>I.7. TRATAMIENTO: La obtención, uso, divulgación o almacenamiento de datos personales, por cualquier medio.</p>



        <p>I.8. TRANSFERENCIA: Toda comunicación de datos realizada a persona distinta del responsable o encargado del tratamiento.</p>



        <p>I.9. COOKIES: Archivo de datos que se almacena en el disco duro del equipo de cómputo o del dispositivo de comunicaciones electrónicas de un usuario al navegar en un sitio de internet específico.</p>



        <p>I.10. DERECHOS ARCO: Son los derechos de acceso, rectificación, cancelación y oposición.</p>



        <p>I.11. ENTORNO DIGITAL: Es el ámbito conformado por la conjunción de hardware, software, redes, aplicaciones, servicios o cualquier otra tecnología de la sociedad de la información que permiten el intercambio o procesamiento informatizado o digitalizado de datos.</p>



        <p>I.12. SITIO WEB: Es el portal de internet del Responsable, mediante el cual se obtiene los datos personales del Titular.</p>



        <p class="has-medium-font-size"><strong>II. OBJETO DEL PRESENTE AVISO DE PRIVACIDAD:</strong></p>



        <p>a) Que los Titulares de los datos estén enterados de la Información que se recaba, como se almacena, quien es el responsable, así como el domicilio del responsable, y cuál es la finalidad de recabar los “Datos Personales” con motivo de la utilización del sitio web de El Responsable.</p>



        <p>b) Hacer de su conocimiento los mecanismos para que el Titular pueda manifestar su negativa para el tratamiento de sus datos personales.</p>



        <p>c) Dar a conocer los medios para que el Titular ejerza sus derechos.</p>



        <p>&nbsp;d) Establecer el mecanismo para que el Titular pueda revocar su consentimiento para el tratamiento de sus datos personales.</p>



        <p>e) Las opciones que ofrece el Titular para limitar el uso o divulgación de sus datos personales.</p>



        <p>f) Cláusula de transferencia.</p>



        <p>g) Medio por el cual el Responsable comunicará al Titular los cambios en el aviso e privacidad.</p>



